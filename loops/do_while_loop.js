/*
do while loop 
 syntax

    do{
     // code to be executed if the condition is true
    }while(condition)

    this loop will execute what is in the do block and then 
    after that it evaluates the while.
    if the condition is true, it will execute what is in the do 
    block again.
    it will continue to do so untill the condition in the while loop
    changes to false.


*/
let number=0
do{
    console.log("The number is", number);
    
    number ++;

}while (number <10);